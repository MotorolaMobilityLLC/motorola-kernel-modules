int wt6670f_isp_flow(struct wt6670f *chip)
{
	u8 chip_id = 0;
	int programed_cnt = 0x0;
	int addr_bit_15_8 = 0x0;
	int addr_bit_7_0 = 0x0;
	u8 *code = NULL;
	int i = 0;
	int len = 0;
	int read_flash_cnt = 0x0;
	u16 fw_ver = 0;
	int ret = -1;

	code = kzalloc(WT6670_WRITE_BYTE, GFP_KERNEL);
	if (code == NULL)
		return -1;
	wt6670f_isp_do_reset();

	wt_enter_isp_mode_sequence(chip);

	msleep(10);
	//while(check_i2c_bus_free);

	wt_enable_isp(chip);

	wt_read_chip_id(chip, &chip_id, 1);
	if (chip_id != 0x70)
	{
		pr_info("[%s]wt6670 err, chip_id:%d, skip isp\n", __func__, chip_id);
		goto isp_err;
	}
	pr_info("[%s]wt6670 chip_id=%d \n", __func__, chip_id);

	wt_enable_isp_flash_mode(chip);
	wt_chip_erase(chip);
	msleep(30);
	wt_finish_erase_or_program(chip);

	//wt_enable_isp_flash_mode(chip->client);

	while (programed_cnt < WT70F_QC3p_V02_210326_FBA1_bin_len)
	{
		addr_bit_15_8 = (programed_cnt >> 8) & 0x0f;
		addr_bit_7_0 = programed_cnt % 0x100;

		memset(code, 0, WT6670_WRITE_BYTE);

		wt_set_address_high_byte(chip, addr_bit_15_8);

		if ((programed_cnt + WT6670_WRITE_BYTE) > WT70F_QC3p_V02_210326_FBA1_bin_len)
		{
			len =  WT70F_QC3p_V02_210326_FBA1_bin_len % WT6670_WRITE_BYTE;
		}
		else
		{
			len = WT6670_WRITE_BYTE;
		}

		for (i = 0; i < len; i++)
		{
			code[i] = WT70F_QC3p_V02_210326_FBA1_bin[programed_cnt + i];
		}

		if (len != WT6670_WRITE_BYTE)
		{
			for (i = len; i < WT6670_WRITE_BYTE; i++)
			{
				code[i] = 0xff;
			}
		}

		wt_program_flash_64byte(chip, addr_bit_7_0, code, WT6670_WRITE_BYTE);

		wt_finish_erase_or_program(chip);
		programed_cnt += WT6670_WRITE_BYTE;
	}

	pr_info("[%s] finish program flash, start verify...\n", __func__);

	//wt_enable_isp_flash_mode(chip->client);

	while (read_flash_cnt < 0x1000)
	{
		addr_bit_15_8 = (read_flash_cnt >> 8) & 0x0f;
		addr_bit_7_0 = read_flash_cnt % 0x100;

		memset(code, 0, WT6670_WRITE_BYTE);

		wt_set_address_high_byte(chip, addr_bit_15_8);
		wt_read_flash_64byte(chip, addr_bit_7_0, code, WT6670_WRITE_BYTE);

		if (read_flash_cnt > WT70F_QC3p_V02_210326_FBA1_bin_len)
		{

		}
		else if (read_flash_cnt + WT6670_WRITE_BYTE > WT70F_QC3p_V02_210326_FBA1_bin_len)
		{
			for (i = 0; i < WT70F_QC3p_V02_210326_FBA1_bin_len % WT6670_WRITE_BYTE; i++)
			{
				if (code[i] != WT70F_QC3p_V02_210326_FBA1_bin[read_flash_cnt + i])
				{
					pr_info("[%s] verify flash data failed, fail_addr:0x%x, fail_data:0x%x, correct_data:0x%x\n", __func__, read_flash_cnt + i,
						code[i], WT70F_QC3p_V02_210326_FBA1_bin[read_flash_cnt + i]);
						goto isp_err;
				}
			}

			//need to verify 0xff
		}
		else
		{
			for (i = 0; i < WT6670_WRITE_BYTE; i++)
			{
				if (code[i] != WT70F_QC3p_V02_210326_FBA1_bin[read_flash_cnt + i])
				{
					pr_info("[%s] verify flash data failed, fail_addr:0x%x, fail_data:0x%x, correct_data:0x%x\n", __func__, read_flash_cnt + i,
						code[i], WT70F_QC3p_V02_210326_FBA1_bin[read_flash_cnt + i]);
						goto isp_err;
				}
			}
		}

		read_flash_cnt += WT6670_WRITE_BYTE;
	}
	pr_info("[%s] firmware update success!!!\n", __func__);
	ret = 0;

isp_err:
	wt_restart_chip(chip);
	kfree(code);
	//do not comment this line
	msleep(10);
	fw_ver = wt6670f_get_firmware_version();
	pr_info("%s: wt6670f firmware:%d!\n", __func__,fw_ver);
	return ret;
}
